package com.efir.client

import android.app.admin.DeviceAdminReceiver

/**
 * Точка входа для назначения приложения Device Owner.
 *
 * Без этого ресивера команда `dpm set-device-owner` просто не отработает —
 * ей нужен компонент администратора устройства. А без Device Owner Android не
 * даёт ставить APK молча: система показывает диалог подтверждения, и экран,
 * к которому никто не подходит, так и останется на старой версии.
 *
 * Включается ТОЛЬКО на свежесброшенном устройстве без добавленных аккаунтов:
 *
 *   adb shell dpm set-device-owner com.efir.client/.EfirDeviceAdminReceiver
 *
 * На уже настроенном телевизоре задним числом — никак, только factory reset.
 * Поэтому решать про Device Owner нужно до развёртывания парка экранов.
 *
 * Своей политики администрирования мы не навязываем: ресивер нужен ровно как
 * компонент для провижининга, поведение по умолчанию нас устраивает.
 */
class EfirDeviceAdminReceiver : DeviceAdminReceiver()
